defmodule ChatServiceWeb.Telemetry do
  use Supervisor
  import Telemetry.Metrics

  def start_link(arg) do
    Supervisor.start_link(__MODULE__, arg, name: __MODULE__)
  end

  @impl true
  def init(_arg) do
    children = [
      {:telemetry_poller, measurements: periodic_measurements(), period: 10_000}
    ]

    Supervisor.init(children, strategy: :one_for_one)
  end

  def metrics do
    [
      # Phoenix Metrics
      summary("phoenix.endpoint.start.system_time", unit: {:native, :millisecond}),
      summary("phoenix.endpoint.stop.duration", unit: {:native, :millisecond}),
      summary("phoenix.router_dispatch.start.system_time", unit: {:native, :millisecond}),
      summary("phoenix.router_dispatch.stop.duration", tags: [:route], unit: {:native, :millisecond}),
      counter("phoenix.router_dispatch.stop.duration", tags: [:route]),

      # LiveView Metrics
      summary("phoenix.live_view.mount.start.system_time", unit: {:native, :millisecond}),
      summary("phoenix.live_view.mount.stop.duration", unit: {:native, :millisecond}),
      summary("phoenix.live_view.handle_event.start.system_time", unit: {:native, :millisecond}),
      summary("phoenix.live_view.handle_event.stop.duration", tags: [:event], unit: {:native, :millisecond}),

      # Channel Metrics
      summary("phoenix.channel_joined.duration", unit: {:native, :millisecond}),
      summary("phoenix.channel_handled_in.duration", tags: [:event], unit: {:native, :millisecond}),

      # Ecto Metrics
      summary("chat_service.repo.query.total_time", unit: {:native, :millisecond}),
      summary("chat_service.repo.query.decode_time", unit: {:native, :millisecond}),
      summary("chat_service.repo.query.query_time", unit: {:native, :millisecond}),
      summary("chat_service.repo.query.queue_time", unit: {:native, :millisecond}),
      summary("chat_service.repo.query.idle_time", unit: {:native, :millisecond}),
      counter("chat_service.repo.query.total_time"),
      distribution("chat_service.repo.query.total_time", unit: {:native, :millisecond}),

      # Chat Service Metrics
      summary("chat_service.message.process.duration", unit: {:native, :millisecond}),
      counter("chat_service.message.received.count"),
      counter("chat_service.message.processed.count"),
      counter("chat_service.message.error.count"),

      # HTTP Client Metrics
      summary("finch.request.stop.duration", tags: [:host], unit: {:native, :millisecond}),
      counter("finch.request.stop.duration", tags: [:host]),

      # VM Metrics
      summary("vm.memory.total", unit: :byte),
      summary("vm.total_run_queue_lengths.total"),
      summary("vm.total_run_queue_lengths.cpu"),
      summary("vm.total_run_queue_lengths.io")
    ]
  end

  defp periodic_measurements do
    [
      {__MODULE__, :dispatch_metrics, []}
    ]
  end

  def dispatch_metrics do
    :telemetry.execute([:vm, :memory], %{total: :erlang.memory(:total)}, %{})
  end
end
